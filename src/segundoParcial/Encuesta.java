    //esta funcion valida si el array cuenta con puros unos o no...
    public static boolean validarArrayDeUnos(int[] array) {
        for (int i = 0; i < array.length; i++) {
            if (array[i] != 1) {
                return false; // Si se encuentra un valor distinto de 1, el array no cumple la condición
            }
        }
        return true; // Si todos los elementos del array son 1, se cumple la condición
    }
    //Esta funcion sirve para ingresar el correo en JOptionpane, seguira en el while hasta que el formato sea correcto...
    public static String ingresarCorreo() {
        String correo = JOptionPane.showInputDialog(null, "Ingrese su correo electrónico:");
        while (!validarCorreo(correo)) {
            JOptionPane.showMessageDialog(null, "Correo electrónico inválido. Intente nuevamente.");
            correo = JOptionPane.showInputDialog(null, "Ingrese su correo electrónico:");
        }
        return correo;
    }
    //Este valida si el formato de correo es correcto o no...
    public static boolean validarCorreo(String correo) {
        // Patrón de expresión regular para validar el correo electrónico
        String patron = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$";    
        return correo.matches(patron);
    }
