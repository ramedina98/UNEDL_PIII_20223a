    private void jc_nacionalidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jc_nacionalidadActionPerformed
        String opcion = (String) jc_nacionalidad.getSelectedItem();
        
        
        if(opcion != null && opcion.equals("Extranjero")){
            boolean esDocumentoValido = false;
            String doc = null;
            String[] opcionesDocumentos = {"Pasaporte", "Visa de Trabajo", "Visa Humanitaria", "Visa H. Con Permiso de Trabajo", 
                "Residencia", "Visa de Estudiante"};
            while (!esDocumentoValido) {
                // Mostrar el cuadro de diálogo de entrada
                int documentoIngresado = JOptionPane.showOptionDialog(
                null,
                "Seleccione el tipo de documento:",
                "Información",
                JOptionPane.DEFAULT_OPTION,
                JOptionPane.INFORMATION_MESSAGE,
                null,
                opcionesDocumentos,
                opcionesDocumentos[0]);

                // Validar el documento ingresado
                switch (documentoIngresado) {
                case 0: doc = "Pasaporte"; break; 
                case 1: doc = "Visa de Trabajo"; break; 
                case 2: doc = "Visa Humanitaria"; break;
                case 3: doc = "Visa H. Con Permiso de Trabajo"; break;  
                case 4: doc = "Residencia"; break; 
                case 5: doc = "Visa de Estudiante"; break; 
                default: 
                    full[5] = 0;
                    esDocumentoValido = false;
                    JOptionPane.showMessageDialog(null, "Documento migratorio inválido. Intente nuevamente.");
                break;
                }
                if(documentoIngresado >= 0 && documentoIngresado <= 50){
                    esDocumentoValido = true; 
                    full[5] = 1; 
                }
            }
            documento = doc;
            persona.setEstadoMigratorio(documento);
        } 
        else{   
            full[5] = 1;
            persona.setEstadoMigratorio("Acta de nacimiento");
        }
    }//GEN-LAST:event_jc_nacionalidadActionPerformed

    private void Btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_guardarActionPerformed
        
        //Aquí iniciamos con genero...
        // Variable para almacenar la opción seleccionada
        String opcionGenero = null;

        // Verificar qué opción está seleccionada
        if (Rb1Genero.isSelected()) {
            opcionGenero = "Masculino";
        } 
        else if (Rb2Genero.isSelected()) {
            opcionGenero = "Femenino";
        } 
        else if (Rb3Genero.isSelected()) {
            opcionGenero = "Otro";
        }
        //mandamos la opcion al objeto...
        if(opcionGenero == null){
            full[0] = 0;
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna opción de genero.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            persona.setGenero(opcionGenero);
            full[0] = 1;
        }
        //Terminamos el campo de genero... 
        
        //iniciamos con el campo de ultimo grado de estudios
        String last = null;
        int indexGrado = Cm_grado.getSelectedIndex();

        //Se lo mandamos al objeto... 
        if(indexGrado == 0){
            full[1] = 0;
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna opción de ultimo grado de estudios", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            last = (String) Cm_grado.getSelectedItem();
            persona.setUltimoGrado(last);
            full[1] = 1;
        }
        //Aqui termina el campo de ultimo grado... 
        
        //Campo de nombre de la escual del ultimo grado de estudios...
        String escuela;
        escuela = txt_nomEscuela.getText();
        if(escuela.isEmpty()){
            full[2] = 0;
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna opción de escuela", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            //enviamos la informacion al objeto... 
            persona.setNamEscuela(escuela);
            full[2] = 1;
        }
        
        //Campo de estado civil...  
        String opcionCivil = null;

        // Verificar qué opción está seleccionada
        if (jr_soltero.isSelected()) {
            opcionCivil = "Soltero";
        } 
        else if (jr_casado.isSelected()) {
            opcionCivil = "Casado";
        } 
        else if (jr_divorciado.isSelected()) {
            opcionCivil = "Divorciado";
        } 
        else if(jr_libre.isSelected()){
            opcionCivil = "Union libre";
        }
        else if(jr_otro.isSelected()){
            opcionCivil = "Otro";
        }
        
        if(opcionCivil == null){
            full[3] = 0;
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna opción de estado civil.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            //mandamos la opcion al objeto...
            persona.setEstadoCivil(opcionCivil);
            full[3] = 1;
        }
        //Terminamos el campo de estado civil... 
        
        //Campo de nacionalidad...
        String nacionalidad = (String) jc_nacionalidad.getSelectedItem();
        int indexNacionalidad = jc_nacionalidad.getSelectedIndex();
        if(indexNacionalidad == 0){
            full[4] = 0;
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ningun opción de nacionalidad.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            //Se lo mandamos al objeto... 
            persona.setNacionalidad(nacionalidad);
            full[4] = 1;
        }
        //Aqui termina el campo de nacionalidad... 
        
        //Aqui empezamos con los check boxes...
        boolean[] isSelected = new boolean[10];//En este array se guarda true o false... 
        String[] infoChecks = {"Cine", "Musica", "Fotbal", "Comer", "Viajar", "Leer", 
            "Lucha Libre", "Programar", "Catar Cerveza", "Carne Asada"};
        // Verificar si la casilla de verificación 1 está seleccionada
        isSelected[0] = jC_cine.isSelected();
        // Verificar si la casilla de verificación 2 está seleccionada
        isSelected[1] = jC_musica.isSelected();
        // Verificar si la casilla de verificación 3 está seleccionada
        isSelected[2] = jC_fot.isSelected();
        // Verificar si la casilla de verificación 1 está seleccionada
        isSelected[3] = jC_comer.isSelected();
        // Verificar si la casilla de verificación 2 está seleccionada
        isSelected[4] = jC_viajar.isSelected();
        // Verificar si la casilla de verificación 3 está seleccionada
        isSelected[5] = jC_leer.isSelected();
        // Verificar si la casilla de verificación 1 está seleccionada
        isSelected[6] = jC_lucha.isSelected();
        // Verificar si la casilla de verificación 2 está seleccionada
        isSelected[7] = jC_programar.isSelected();
        // Verificar si la casilla de verificación 3 está seleccionada
        isSelected[8] = jC_catar.isSelected();
        // Verificar si la casilla de verificación 3 está seleccionada
        isSelected[9] = jC_asada.isSelected();
        //En este for checaremos cuales son true y cuales false, dependiendo de la posicion... 
        String allChecks = null; 
        int y = 0; 
        for(int i = 0; i < 10; i++){
            if(isSelected[i] == true){
                y++; 
                if(y == 1){
                    allChecks = " - " + infoChecks[i] + " - "; 
                }
                else if(y > 1){
                    allChecks += infoChecks[i] + " - ";
                }
            }
        }
        
        if(allChecks == null){
            full[6] = 0;
            JOptionPane.showMessageDialog(null, "Debe seleccionar por lo menos una opción.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            //mandamos la info al campo correspondiente en el objeto... 
            persona.setIntereses(allChecks);
            full[6] = 1;
        }
        
        /*hay que validar que la información de todos los campos ya fue recolectada y que ningun String es null...*/
        boolean esValido = validarArrayDeUnos(full);
        
        if(esValido){
            String correo = ingresarCorreo();
            persona.setEmail(correo);
            //Aquí se estan imprimiendo en la terminal todas las opciones que se guardaron...
            String reg = persona.getGenero() + " , " + persona.getUltimoGrado() + " , " + persona.getNamEscuela() + " , " + persona.getEstadoCivil() + 
                    " , " + persona.getNacionalidad() + " , " + persona.getEstadoMigratorio() + " , " + persona.getIntereses() + " , " + 
                    persona.getEmail();
            System.out.println("Datos: " + reg);
            try {
                File file = new File(CSV_FILE_PATH);

                // Verificar si el archivo CSV ya existe
                boolean existeArchivo = file.exists();
                

                // Crear un FileWriter en modo de adición (append)
                FileWriter fileWriter = new FileWriter(file, true);
                PrintWriter writer = new PrintWriter(fileWriter);

                // Escribir el encabezado solo si el archivo no existe
                if (!existeArchivo) {
                    writer.println("Genero,Grado de estudio,Nom. Escuela,Estado Civil,Nacionalidad,Documento,Intereses, Email");
                }

                // Escribir la información en una nueva fila
                writer.println(reg);

                writer.close();

                System.out.println("La información se ha agregado al archivo encuestas.cvs");

            } 
            catch (IOException e) {
                System.out.println("Error al escribir en el archivo CSV: " + e.getMessage());
            }
            
            //Limpiamos todos los campos para que queden listos para volver a llenarlos...
            //genero...
            grupoBtnsGenero.clearSelection();
            //ultimo grados de estudios...
            Cm_grado.setSelectedIndex(0);
            //Nombre de escuela...
            txt_nomEscuela.setText("");
            //estado civil...
            grupoBtnsEdoCivil.clearSelection();
            //nacionalidad...
            jc_nacionalidad.setSelectedIndex(0);
            //gustos e intereses...
            jC_cine.setSelected(false);
            jC_musica.setSelected(false);
            jC_fot.setSelected(false);
            jC_comer.setSelected(false);
            jC_viajar.setSelected(false);
            jC_leer.setSelected(false);
            jC_lucha.setSelected(false);
            jC_programar.setSelected(false);
            jC_catar.setSelected(false);
            jC_asada.setSelected(false);
        }
        else{
            JOptionPane.showMessageDialog(null, "No se puede guardar hasta tener todos los datos recopilados.", "Advertencia", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Btn_guardarActionPerformed

